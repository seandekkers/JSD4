function calcFahrenheitToCelcius(f){
return ((f - 32) * 5/9);
}

function calcCelciusToFarenheit(c){
return (c * (9/5) + 32);	
}


function calcCircumference(r){
return ((r*2) * Math.PI);
}

function calcLongestSide(a,b){ 
	return Math.sqrt((a * a) + (b * b));

}
